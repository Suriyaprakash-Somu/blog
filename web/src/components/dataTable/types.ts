// =============================================================================
// Export Configuration Types
// =============================================================================

/**
 * Export handler function signature
 */
export type ExportHandlerFunc<TData> = (
  tag: string,
  rows: TData[],
  visibleColumns: ColumnDef<TData>[]
) => void | Promise<void>;

/**
 * Export configuration for table-level bulk exports
 */
export interface ExportConfig<TData = unknown> {
  /** Export handler function */
  handler: ExportHandlerFunc<TData>;
  
  /** Button label (default: format name) */
  label?: string;
  
  /** Icon to display */
  icon?: ReactNode;
  
  /** Tooltip text */
  tooltip?: string;
}

/**
 * Exports configuration - separate from operations
 */
export interface ExportsConfig<TData> {
  pdf?: ExportConfig<TData>;
  csv?: ExportConfig<TData>;
  excel?: ExportConfig<TData>;
  json?: ExportConfig<TData>;
  xml?: ExportConfig<TData>;
  // Allow custom export formats
  [key: string]: ExportConfig<TData> | undefined;
}

// =============================================================================
// Main DataTable Props
// =============================================================================

export interface DataTableProps<TData extends RowWithId> {
  tag: string;
  title: string;
  subject?: AppSubject;
  moduleKey?: string;
  
  /** Data fetching configuration */
   fetchData: {
     endpoint: string;
     method?: "GET" | "POST";
     key: string | readonly unknown[];
     params?: QueryParams;
   };
  
  columnsConfig: ColumnGroup<TData>[];
  
  /** Unified operations configuration (Row-level) */
  operations?: OperationsConfig<TData>;

  /** Table-level bulk exports (PDF, CSV, Excel, etc.) */
  exports?: ExportsConfig<TData>;
  
  organizationId?: string;
  organizationRequired?: boolean;
  filterConfig?: FilterConfig;
  
  /** Legacy or optional overrides */
  showPagination?: boolean;
  dontShowColumnSelect?: boolean;
  actionHeader?: string;
  
  /** 
   * Validation mode for operations
   * - 'strict': Throw errors if multiple execution methods provided (recommended)
   * - 'priority': Use priority order (component > api > handler) with warnings
   * @default 'strict'
   */
  validationMode?: "strict" | "priority";
}

export type ModalType = OperationType | string | null;
