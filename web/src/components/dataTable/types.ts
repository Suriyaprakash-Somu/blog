// =============================================================================
// Operation Configuration Types (Unified Protocol)
// =============================================================================

export type OperationType = "add" | "edit" | "view" | "delete" | "complete" | "dismiss";

export type DisplayMode = "dialog" | "sheet";

/**
 * Props passed to operation components
 */
export interface OperationComponentProps<TData> {
  data?: TData;
  onSuccess: () => void;
  onCancel: () => void;
}

/**
 * Base configuration for any operation
 * IMPORTANT: Only ONE of component, api, or handler should be provided
 */
export interface BaseOperationConfig<TData = unknown> {
  /** Option 1: Custom React component for full UI control */
  component?: ComponentType<OperationComponentProps<TData>>;
  
  /** Option 2: API endpoint configuration for server calls */
  api?: {
    endpoint: string;
    method?: "POST" | "PUT" | "DELETE" | "PATCH"; // GET removed as it is not a mutation
    key: string | readonly unknown[];
    /** Next.js cache tags to revalidate after mutation */
    revalidateNextTags?: string[];
    /** Next.js paths to revalidate after mutation */
    revalidatePaths?: string[];
  };

  /** Option 3: Custom function handler for client-side logic */
  handler?: (data?: TData) => void | Promise<void>;
  
  /** Optional: Confirmation settings */
  confirmation?: {
    title?: string;
    message?: string;
    confirmText?: string;
    cancelText?: string;
  };
  
  /** Display mode for component-based operations */
  displayMode?: DisplayMode;

  /** Width size for sheet/dialog: 'sm' | 'md' | 'lg' | 'xl' | 'full' */
  width?: 'sm' | 'md' | 'lg' | 'xl' | 'full';
  
  /** Label for buttons (e.g. "Add User") */
  label?: string;
  
  /** Custom title for the modal (overrides auto-generated title) */
  modalTitle?: string;

  /** Custom description for the modal (overrides auto-generated description) */
  modalDescription?: string;

  icon?: ReactNode;

  /** Conditional display for row-level actions */
  isVisible?: (data: TData) => boolean;

  /** Override modal requirement (default: true for component, false for handler) */
  requiresModal?: boolean;
}

/**
 * Validated operation config - ensures only one execution method
 */
export type ValidatedOperationConfig<TData> = 
  | { component: ComponentType<OperationComponentProps<TData>>; api?: never; handler?: never }
  | { api: NonNullable<BaseOperationConfig<TData>['api']>; component?: never; handler?: never }
  | { handler: NonNullable<BaseOperationConfig<TData>['handler']>; component?: never; api?: never };

/**
 * Complete operations registry
 */
export interface OperationsConfig<TData> {
  add?: BaseOperationConfig<TData>;
  edit?: BaseOperationConfig<TData>;
  view?: BaseOperationConfig<TData>;
  delete?: BaseOperationConfig<TData>;
  complete?: BaseOperationConfig<TData>;
  dismiss?: BaseOperationConfig<TData>;
  [key: string]: BaseOperationConfig<TData> | undefined;
}

