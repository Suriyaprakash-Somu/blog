// =============================================================================
// Column Configuration Types
// =============================================================================

export interface ColumnMeta {
  thClass?: string;
  tdClass?: string;
  align?: "left" | "center" | "right";
  width?: string;
  [key: string]: unknown;
}

export interface ColumnConfig<TData> {
  id?: string;
  accessorKey?: keyof TData & string;
  header?: string | ((context: HeaderContext<TData, unknown>) => ReactNode);
  cell?: (context: CellContext<TData, unknown>) => ReactNode;
  enableSorting?: boolean;
  enableHiding?: boolean;
  headerClassName?: string;
  cellClassName?: string;
  className?: string;
  meta?: ColumnMeta;
}

export interface ColumnGroup<TData> {
  id?: string;
  header?: string | null;
  columns: ColumnConfig<TData>[];
  meta?: ColumnMeta & {
    isAutoGeneratedGroup?: boolean;
  };
}

